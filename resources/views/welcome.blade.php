					<h2>The Legend of Our Namesake; Mount Konocti</h2>
				</header>
				<p>Mount Konocti has been admired, respected, and feared for many centuries by both Native American tribes and white settlers alike.</p>
				<p>The name "Konocti" is derived from the Pomo word <strong><i>kno</i></strong>, mountain, and <strong><i>htai</i></strong>, woman; roughly translated as <strong><i>Mountain Woman</i></strong>. One Native American legend credits the name to a woman who, after quarreling with her husband, stormed off and told him he would "never see her alive again." Members of the tribe found her body several days later on the mountain, having been crushed to death by a giant king snake. Native Americans held a deep respect for the mountain and would hold sacred ceremonies on it, but never set up any permanent dwellings there.</p>
				<p>A dormant (not extinct) volcano, Mount Konocti is said by geologists to be a composite cone of lava that was built up over millions of years of gentle eruptions. The volcano became dormant approximately 10,000 years ago, making it the youngest mountain in all of California's coastal ranges.</p>
				<p>The Konocti soil is extraordinarily porous â€” so much so that rain falling on the mountain never forms into any kind of creek or stream. It simply absorbs directly into the mountain. The same is somewhat true here on our farm as the ground has, for the most part, a combination composed of organic matter, lava rock and ash - great for growing tomatoes and grapes.</p>
				<p>A story coming from a feature article published in the magazine of the California Academy of Science by writer, Juliane Poirier Locke: ''... Inside Mt. Konocti's cooled magma chambers the world's largest cavern and tallest volcanic shaft may await discovery ...'<br />'In the 1990s curious scientists dived and descended into an opening. They described a pulsing cycle during which a few minutes of vigorous bubbling alternated with modest bubbling. A strong water current helped push the divers upward toward the surface, while another current along the walls of the opening pulled downward into a lake bed fault. At about sixty-five feet into the hole, the narrow tube broadened out into a twenty-foot chasm where the water was forced through wet clay ...'</p>
				<p>Lake County Historian, Henry Mauldin, reported, 'Those who crawled into the cave (into Konocti) found it extended into a horizontal tunnel that ended abruptly at a vertical shaft. Painted cans and marked sticks dropped into the shaft never hit bottom. Days and weeks later the markers were found afloat in Clear Lake.' That elusive shaft, never found as yet, may be the highest underground vertical drop on Earth; over two thousand feet straight down.'</p>
